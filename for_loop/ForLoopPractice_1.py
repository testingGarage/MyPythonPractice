
# # For loop iterating by knowing the length of  List.

bugType = ["functional", "ui", "security", "performance", "ux", "usability", "accessibility", "compatibility", "not known"]

for qualityCriteria in range(len(bugType)):
    print("The index of Bug Type", bugType[qualityCriteria], "is", qualityCriteria)

# The index of Bug Type functional is 0
# The index of Bug Type ui is 1
# The index of Bug Type security is 2
# The index of Bug Type performance is 3
# The index of Bug Type ux is 4
# The index of Bug Type usability is 5
# The index of Bug Type accessibility is 6
# The index of Bug Type compatibility is 7
# The index of Bug Type not known is 8




